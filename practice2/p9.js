// let 변수 선언

// 변수
// 변수에 특정한 값을 저장할 수 있다
// 데이터는 저장을 하지 않으면 재사용할 수 없다

1 + 4 // 5
// 5를 계속 사용하기 위해서는 변수에 저장해야 한다

let sum = 1 + 4;
console.log(sum + 5); // 5를 변수 sum에 저장해서 재사용


// 변수 선언
let variable;

// 변수 초기화
variable = "I am Variable";

// 변수 선언과 동시에 초기화
let num = 10;

// let => 선언문
// num => 변수명
// = => 대입 연산자
// 10 => 값